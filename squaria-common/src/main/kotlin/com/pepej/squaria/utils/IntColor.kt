    fun setAlpha(color: Int, alpha: Int): Int {
        return (color and 16777215) + (alpha shl 24)
    }

    fun getAlpha(color: Int): Int {
        return color shr 24 and 255
    }

    fun fromRGB(red: Int, green: Int, blue: Int): Int {
        return fromARGB(255, red, green, blue)
    }

    fun fromARGB(alpha: Int, red: Int, green: Int, blue: Int): Int {
        return alpha shl 24 or (red shl 16) or (green shl 8) or blue
    }

    fun alterBrightness(color: Int, delta: Int): Int {
        var r = color shr 16 and 255
        var g = color shr 8 and 255
        var b = color and 255
        r = Math.min(255, r + delta)
        g = Math.min(255, g + delta)
        b = Math.min(255, b + delta)
        return (color and -16777216) + (r shl 16) + (g shl 8) + b
    }
